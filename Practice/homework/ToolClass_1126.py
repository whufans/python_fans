# 1：自己写一个工具类，完成数学的加减乘除以及平方积操作
class ToolClass:
	def  add(self,a,b):
		return a+b
	def  subtract(self,a,b):
		return a-b
	def  multiple(self,a,b):
		return a*b
	def divide(self,a,b):
		return  a/b
	def  squareProduct(self,a,b):
		return  (a**2)*(b**2)

